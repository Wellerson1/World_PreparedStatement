
package aps_word;

public class Pais {
    String nome;
    String code;
    String Continente;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getContinente() {
        return Continente;
    }

    public void setContinente(String Continete) {
        this.Continente = Continete;
    }

}
